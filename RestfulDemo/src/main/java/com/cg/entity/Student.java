package com.cg.entity;

public class Student {
	
	public int id;
	public String name;
	public String dept;
	public String univ;
	
	public Student()
	{
		
	}
	public Student(int id, String name, String dept, String univ) {
		super();
		this.id = id;
		this.name = name;
		this.dept = dept;
		this.univ = univ;
	}
	
	

}
